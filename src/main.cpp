void pre_auton(void) {
  // Initializing Robot Configuration. DO NOT REMOVE!
  vexcodeInit();
  // All activities that occur before the competition starts
  // Example: clearing encoders, setting servo positions, ...
  scraper.set(true); // Set scraper to false at the start
  goalExtended = true; // Initialize goalExtended to false 
}

/*---------------------------------------------------------------------------*/
/*                                                                           */
/*                              Autonomous Task                              */
/*                                                                           */
/*  This task is used to control your robot during the autonomous phase of   */
/*  a VEX Competition.                                                       */
/*                                                                           */
/*  You must modify the code to add your own robot specific commands here.   */
/*---------------------------------------------------------------------------*/

void autonomous(void) {
  Drivetrain.driveFor(-25.622, inches);
  scraper.set(true);
  Intake.spin(fwd, 80, percentUnits::pct);
  storage.spin(fwd, 80, percentUnits::pct);
  top.spin(fwd, 80, percentUnits::pct);
  wait(2, seconds); 
}


/*---------------------------------------------------------------------------*/
/*                                                                           */
/*                              User Control Task                            */
/*                                                                           */
/*  This task is used to control your robot during the user control phase of */
/*  a VEX Competition.                                                       */
/*                                                                           */
/*  You must modify the code to add your own robot specific commands here.   */
/*---------------------------------------------------------------------------*/

void usercontrol(void) {
  Controller1.Screen.clearScreen();
  Controller1.Screen.setCursor(1,1);

  Brain.Screen.setPenColor(green);
  Brain.Screen.setFillColor(blue);
  Brain.Screen.drawRectangle(10, 10, 230, 220);
  Brain.Screen.setFillColor(red);
  Brain.Screen.drawRectangle(240, 10, 230, 220);
  Brain.Screen.setFont(fontType::prop60);
  while (1) {

   
    wait(20, msec); // Sleep the task for a short amount of time to
                    // prevent wasted resources.
  }
}

//
// Main will set up the competition functions and callbacks.
//
int main() {
  // Set up callbacks for autonomous and driver control periods.
  Competition.autonomous(autonomous);
  Competition.drivercontrol(usercontrol);

  // Run the pre-autonomous function.
  pre_auton();

  // Prevent main from exiting with an infinite loop.
  while (true) {
    wait(100, msec);
  }
}
